cifar_train = ([(cifar_train_data.features[:, :, :, i], cifar_labels[:, i]) for i in Iterators.partition(1:50000, 1000)]) |> gpu

# ╔═╡ ffd6d306-a771-4fe0-9bab-57e981bb4c5d
