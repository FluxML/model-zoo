# ╔═╡ 98837594-4421-43c6-9d70-256dbd126318
md"### Defining the Classifier
Now we can define our Convolutional Neural Network (CNN).

A convolutional neural network is one which defines a kernel and slides it across a matrix
to create an intermediate representation to extract features from. It creates higher order
features as it goes into deeper layers, making it suitable for images, where the structure of
the subject is what will help us determine which class it belongs to."

# ╔═╡ f05f1469-3b91-434e-929c-17a030ae0565
cifar_model = Chain(
    Conv((5, 5), 3 => 16, relu),
    MaxPool((2, 2)),
    Conv((5, 5), 16 => 8, relu),
    MaxPool((2, 2)),
    x -> reshape(x, :, size(x, 4)),
    Dense(200, 120),
    Dense(120, 84),
    Dense(84, 10),
    softmax) |> gpu

# ╔═╡ f5211ac3-354e-446f-aa0a-1955f0e57c8d
begin
    using OneHotArrays, Statistics
    cifar_accuracy(x, y) = mean(onecold(cifar_model(x), 0:9) .== y)
end

# ╔═╡ ee50542a-a0c1-48f9-be5e-40453576b9ba
md"We will use a `crossentropy` loss and the `Momentum` optimiser here. Crossentropy will be a
good option when it comes to working with multiple independent classes. Momentum smooths out
the noisy gradients and helps towards a smooth convergence. Gradually lowering the 
learning rate along with momentum helps to maintain a bit of adaptivity in our optimisation,
preventing us from overshooting our desired destination."

# ╔═╡ 9a19e9bc-5fd4-4d88-a125-b963321d9e9b
begin
    cifar_loss(x, y) = sum(Flux.Losses.crossentropy(cifar_model(x), y))
    cifar_opt = Momentum(0.03)
end

# ╔═╡ 18e17600-b2b3-4fd3-a03d-bd4c570db962
md"We can start writing our train loop where we will keep track of some basic accuracy
numbers about our model. We can define an `accuracy` function for it like so."

# ╔═╡ ba2e433e-f0a0-45e8-90b1-eb4e3bd21068
md"### Training

Training is where we do a bunch of the interesting operations we defined earlier,
and see what our net is capable of. We will loop over the dataset 10 times and
feed the inputs to the neural network and optimise."

# ╔═╡ eec97769-b4b4-4aa4-b12e-0bb215d58fcc
begin
    epochs = 10

    for epoch = 1:epochs
        for d in cifar_train
            gs = gradient(Flux.params(cifar_model)) do
                l = cifar_loss(d...)
            end
            Flux.update!(opt, Flux.params(cifar_model), gs)
        end
        @info "accuracy: $(cifar_accuracy(cifar_test.features, cifar_test.targets))"
    end
end

