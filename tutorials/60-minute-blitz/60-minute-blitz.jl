# ╔═╡ 28d62259-9049-465f-a654-ec0358826c2b
md"This gets more interesting when we consider functions that take *arrays* as
inputs, rather than just a single number. For example, here's a function that
takes a matrix and two vectors (the definition itself is arbitrary)"

# ╔═╡ 5105fc4b-d9e5-4c3f-9b16-e700a7be74bf
begin
    local myloss(W, b, x) = sum(W * x .+ b)

    local W = randn(3, 5)
    local b = zeros(3)
    local x = rand(5)

    Flux.gradient(myloss, W, b, x)
end

# ╔═╡ 4f9c221b-d382-4467-b86f-ef2e3c51aebe
md"Now we get gradients for each of the inputs `W`, `b` and `x`, which will come
in handy when we want to train models.

Because ML models can contain hundreds of parameters, Flux provides a slightly
different way of writing `gradient`. We instead mark arrays with `param` to
indicate that we want their derivatives. `W` and `b` represent the weight and
bias respectively."

# ╔═╡ a1c41e9a-1866-4654-b6a3-9321922db778
md"### Flux.params"

# ╔═╡ 5a07e503-7f49-43ae-afb2-f9e5727f6b06
begin
    local W = randn(3, 5)
    local b = zeros(3)
    local x = rand(5)

    local y(x) = sum(W * x .+ b)

    local grads = Flux.gradient(() -> y(x), Flux.params((W=W, b=b)))

    grads[W], grads[b]
end

# ╔═╡ e51fe4ac-5ced-4f0a-bf20-e10864c931ce
md"We can now grab the gradients of `W` and `b` directly from those parameters if we use named tuples.

This comes in handy when working with *layers*. A layer is just a handy
container for some parameters. For example, `Dense` does a linear transform
for you."

# ╔═╡ c76b9a22-ba54-42da-83ce-770713e001f1
tiny_model = Flux.Dense(10, 5)

# ╔═╡ d5f387af-1a89-4e08-986a-81f5c4d7251c
md"We can easily get the parameters of any layer or model with
`params`:"

# ╔═╡ 7546da12-5ba0-4e05-897e-b9ef918ea3ce
Flux.params(tiny_model)

# ╔═╡ 19546141-95e1-4e8d-8183-c7d0bd4eda2b
md"This makes it very easy to calculate the gradient for all
parameters in a network, even if it has many parameters."

# ╔═╡ 7017a17d-cc25-47ad-ab97-a36724e48f6b
md"### Simple Trainer"

# ╔═╡ f1b11e5d-8cfc-4f42-9dc5-ff164a5a0242
begin
    local x = rand(Float32, 10)
    model = Chain(Dense(10, 5, relu), Dense(5, 2), softmax)
    loss(x) = Flux.Losses.crossentropy(model(x), [0.5, 0.5])
    grads = gradient(Flux.params(model)) do
        loss(x)
    end
    for p in Flux.params(model)
        println(grads[p])
    end
end

# ╔═╡ 5bd758e0-62b6-422d-91b5-d55a8258d533
md"You don't have to use layers, but they can be convenient for many simple kinds
of models and fast iteration.

The next step is to update our weights and perform optimisation. As you might be
familiar, *Gradient Descent* is a simple algorithm that takes the weights and steps
using a learning rate and the gradients. `weights = weights - learning_rate * gradient` 
(note that `Flux.Optimise.update!(x, x̄)` already updates with the negative of `x̄`)."

# ╔═╡ 3c7a0bee-cc55-4ed4-9439-08ffcff0028f
begin
    η = 0.1 # learining rate
    for p in Flux.params(model)
        Flux.update!(p, η * grads[p])
    end
end

# ╔═╡ a9f167c9-0962-467b-9a75-6df12774fcaf
md"While this is a valid way of updating our weights, it can get more complicated as the
algorithms we use get more involved.

Flux comes with a bunch of pre-defined optimisers and makes writing our own really simple.
We just give it the learning rate η"

# ╔═╡ 4c8eebcb-9a08-4e02-855b-f797835fb072
opt = Descent(0.01)

# ╔═╡ a80435e0-57c1-483a-b093-857d6e67d5a5
md"`Training` a network reduces down to iterating on a dataset multiple times, performing these
steps in order. Just for a quick implementation, let’s train a network that learns to predict
`0.5` for every input of 10 floats. `Flux` defines the `train!` function to do it for us."

# ╔═╡ e111fa78-5cd0-4086-a040-9dff31f83167
begin
    data, labels = rand(10, 100), fill(0.5, 2, 100)
    loss(x, y) = Flux.Losses.crossentropy(model(x), y)
    Flux.train!(loss, Flux.params(model), [(data, labels)], opt)
    # XXX: Check Flux docs on not updating some array with train!, drop Flux.params
end

# ╔═╡ f8427698-68a6-416f-b02f-38e15f54c8d4
md"You don't have to use `train!`. In cases where arbitrary logic might be better suited,
you could open up this training loop like so:

```julia
for d in training_set # assuming d looks like (data, labels)
	# our super logic
	gs = gradient(params(m)) do #m is our model
		l = loss(d...)
	end
	update!(opt, params(m), gs)
end
```"

